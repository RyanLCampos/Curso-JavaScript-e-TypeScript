let num1 = 9.54578;


// Arredonda para baixo
console.log(Math.floor(num1));

// Arredonda para cima 
console.log(Math.ceil(num1));

// Arredonda para mais proximo
console.log(Math.round(num1));

// Busca o maior numero
console.log(Math.max(1, 2, 3, 4, 5, 2000, -1, -39)); // 2000

// Busca o menor numero
console.log(Math.min(1, 2, 3, 4, 5, 2000, -1, -39)); // -39

// Gerar numero aleatorio
console.log(Math.random()); // Entre 0 e 1 - Nunca será 1

// Atribui valor de PI
console.log(Math.PI);

// Potenciação
console.log(Math.pow(2, 10)); // 2 elevado a 10
console.log(2 ** 10);

// Raiz Quadrada
console.log(num1 ** 0.5);